// Increment and Decrement Operators

// Postfix
var number = 0;
console.log("postfix")
console.log(number ++);
console.log(number);
console.log(number --);
console.log(number);

// Prefix
var numberTwo = 0;
console.log("prefix")
console.log(++ numberTwo);
console.log(-- numberTwo);

console.log("for loop")
for (var i = 0; i <= 10; i++) {
    console.log(i);
}
